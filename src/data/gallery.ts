// ── Gallery image type ────────────────────────────────────────────────────────

export interface GalleryImage {
  id: string;
  /** Public path served from /public — pass directly to next/image src. */
  url: string;
  altText: string;
  category: ImageCategory;
  sortOrder: number;
  isHero: boolean;
  /** Intrinsic width of the optimized WebP, required by next/image. */
  width: number;
  /** Intrinsic height of the optimized WebP, required by next/image. */
  height: number;
  /**
   * Tiny 10px-wide WebP encoded as base64 data URL.
   * Pass to next/image as `placeholder="blur" blurDataURL={...}`.
   * Generated by scripts/process-images.mjs.
   */
  blurDataURL: string;
}

