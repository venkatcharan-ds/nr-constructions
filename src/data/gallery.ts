// ── Query helpers ─────────────────────────────────────────────────────────────

/** Returns images filtered by category, sorted by sortOrder. */
export function getImagesByCategory(category: ImageCategory): GalleryImage[] {
  return GALLERY_IMAGES.filter((img) => img.category === category).sort(
    (a, b) => a.sortOrder - b.sortOrder,
  );
}

/** Returns the designated hero image. */
export function getHeroImage(): GalleryImage {
  const hero = GALLERY_IMAGES.find((img) => img.isHero);
  if (!hero) throw new Error("No hero image designated in GALLERY_IMAGES");
  return hero;
}
